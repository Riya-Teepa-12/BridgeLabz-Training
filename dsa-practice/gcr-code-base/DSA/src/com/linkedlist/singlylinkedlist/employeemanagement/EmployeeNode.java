package com.linkedlist.singlylinkedlist.employeemanagement;

public class EmployeeNode {
	
	   //attributes
        int id;
        String name;
        String department;
        double salary;
       EmployeeNode next;
       
	   public EmployeeNode(int id, String name, String department, double salary) {
		this.id = id;
		this.name = name;
		this.department = department;
		this.salary = salary;
		this.next = next;
	   }
       
	  
       
       
}
